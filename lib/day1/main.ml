let recover_value (line : string) : int =
  let digits =
    line
    |> String.to_list
    |> List.map ~f:String.of_char
    |> List.filter_map ~f:Int.of_string_opt
  in
  let x, y = List.hd_exn digits, List.last_exn digits in
  (x * 10) + y

let solve (lines : string) : int =
  lines
  |> String.split_lines
  |> List.map ~f:recover_value
  |> List.fold ~init:0 ~f:( + )

