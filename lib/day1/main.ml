(*
--- Part Two ---
Your calculation isn't quite right. It looks like some of the digits are actually spelled out with letters: one, two, three, four, five, six, seven, eight, and nine also count as valid "digits".

Equipped with this new information, you now need to find the real first and last digit on each line. For example:

two1nine
eightwothree
abcone2threexyz
xtwone3four
4nineeightseven2
zoneight234
7pqrstsixteen
In this example, the calibration values are 29, 83, 13, 24, 42, 14, and 76. Adding these together produces 281.

What is the sum of all of the calibration values?   
*)

let rec get_first_digit_exn (s : char list) : int =
  match s with
  | [] -> failwith "no digit found"
  | '0' :: _ | 'z' :: 'e' :: 'r' :: 'o' :: _ -> 0
  | '1' :: _ | 'o' :: 'n' :: 'e' :: _ -> 1
  | '2' :: _ | 't' :: 'w' :: 'o' :: _ -> 2
  | '3' :: _ | 't' :: 'h' :: 'r' :: 'e' :: 'e' :: _ -> 3
  | '4' :: _ | 'f' :: 'o' :: 'u' :: 'r' :: _ -> 4
  | '5' :: _ | 'f' :: 'i' :: 'v' :: 'e' :: _ -> 5
  | '6' :: _ | 's' :: 'i' :: 'x' :: _ -> 6
  | '7' :: _ | 's' :: 'e' :: 'v' :: 'e' :: 'n' :: _ -> 7
  | '8' :: _ | 'e' :: 'i' :: 'g' :: 'h' :: 't' :: _ -> 8
  | '9' :: _ | 'n' :: 'i' :: 'n' :: 'e' :: _ -> 9
  | _ :: s -> get_first_digit_exn s

let get_last_digit_exn (s : char list) : int =
  let rec loop = function
    | [] -> failwith "no digit found"
    | '0' :: _ | 'o' :: 'r' :: 'e' :: 'z' :: _ -> 0
    | '1' :: _ | 'e' :: 'n' :: 'o' :: _ -> 1
    | '2' :: _ | 'o' :: 'w' :: 't' :: _ -> 2
    | '3' :: _ | 'e' :: 'e' :: 'r' :: 'h' :: 't' :: _ -> 3
    | '4' :: _ | 'r' :: 'u' :: 'o' :: 'f' :: _ -> 4
    | '5' :: _ | 'e' :: 'v' :: 'i' :: 'f' :: _ -> 5
    | '6' :: _ | 'x' :: 'i' :: 's' :: _ -> 6
    | '7' :: _ | 'n' :: 'e' :: 'v' :: 'e' :: 's' :: _ -> 7
    | '8' :: _ | 't' :: 'h' :: 'g' :: 'i' :: 'e' :: _ -> 8
    | '9' :: _ | 'e' :: 'n' :: 'i' :: 'n' :: _ -> 9
    | _ :: s -> loop s
  in
  loop (List.rev s)

let recover_value (line : string) : int =
  let chars = line |> String.to_list in
  let x, y = get_first_digit_exn chars, get_last_digit_exn chars in
  (x * 10) + y

let solve (lines : string) : int =
  lines
  |> String.split_lines
  |> List.map ~f:recover_value
  |> List.fold ~init:0 ~f:( + )

